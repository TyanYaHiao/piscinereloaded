int		ft_sqrt(int nb)
{
	int		ans;

	ans = 1;
	if (nb < 0)
		return (0);
	else
	{
		while ((ans * ans) <= nb)
		{
			if ((ans * ans) == nb)
				return (ans);
			ans++;
		}
		if ((ans * ans) > nb)
			return (0);
	}
	return (0);
}
